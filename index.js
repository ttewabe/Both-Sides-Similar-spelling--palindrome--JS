
// write the function to determine if the given string is palindrome.


const string = "racecar"
function palindrome(str){
  var str = str.toLowerCase().replace()
for (let i =0; i<str.length; i++){
  if (str[i] !== str[str.length -1 -i]){
return false
  }
  
}return true
}
